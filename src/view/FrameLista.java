    private void carregarLista(List<Cliente> lista){
        String newline = System.getProperty("line.separator");
        String resultado = "";
        for (Cliente c : lista){
            resultado += "Nome: "+ c.getNome() + " CPF: " + c.getCpf() + " Renda: " + String.valueOf(c.getRenda());
            
            if(c.isAtivo()){
                resultado += " Ativo: Sim";
            } else {
                resultado += " Ativo: NÃ£o";
            }
            resultado += newline;
        }
        txLista.setText(resultado);
    }

